Weboldalunk cookie-kat használ annak biztosítására, hogy Önnek a lehető legjobb élményt nyújtsa.
<h4>Mi is az a Cookie?</h4>A Cookie, vagy magyarosan Süti, olyan információ amely az Ön gépén tárolódik, ez által a weboldal tudja azonosítani Önt.
Például, hogy látogatta-e már meg az oldalt, vagy még nem. Az oldal mely részein mennyi időt töltött, és miket nézett meg.
Ennek tanulmányozásával a weboldalt jobban tudjuk igazítani az Ön igényeihez, és még változatosabb felhasználói élményt tudunk nyújtani Önnek.
Fontos, hogy ezek a cookie-k csak arra az oldalra, vagy az oldal aloldalaira vonatkoznak, tehát máshonnan származó információ
nem kerül be, és nem távozik semmiféle adat más oldalak felé.

Kétféle süti létezik: „ideiglenes” („munkamenet sütiket”) és „állandó sütiket” („személyre szabott sütiket”). Az ideiglenes sütik addig maradnak eszközén,
amíg el nem hagyja weboldalunkat, míg az állandó sütik webes keresőjének beállításától függően hosszabb ideig, vagy egészen addig az eszközén maradnak,
amíg azokat manuálisan nem törli.

A cookiet számítógépéről bármikor törölheti.

<h4>Az oldal által használt sütik: </h4>Állandó sütik.

<h4>Az adatgyűjtés célja:</h4>Online foglalásával kapcsolatban kezeljük a felhasználó személyes adatait.
Valamint a hírlevélre feliratkozott felhasználók bizonyos adatait.

<h4>Hozzájárulás a sütikhez:</h4>Amennyiben használja az oldalunkat, jóváhagyja, hogy sütiket használjunk.
Az általunk beállított sütik célja minden esetben a felhasználó által igényelt funkció működésének biztosítása, vagy a felhasználói élmény fokozása.
